#!/usr/bin/env bash
# shellcheck shell=bash
#
# loop_lib.sh -- shared helpers for the observability / capture-loop
# orchestrators (observability-loop.sh, baseline-snapshot.sh,
# run-baseline-tour.sh, stress-campaign.sh).
#
# Source from a caller after setting set -uo pipefail (or -euo, the
# helpers are tolerant of either):
#
#   REPO_ROOT="$(cd "$(dirname "$0")/../.." && pwd)"
#   . "${REPO_ROOT}/scripts/lib/loop_lib.sh"
#
# Helpers expect the caller to populate the env vars they reference
# (QCOW, PIDFILE, WINRM_PORT, ADMIN_USER, ADMIN_PW, etc.) before invoking
# the function. This keeps the lib free of "magic" hardcoded defaults --
# the orchestrators are the source of truth for credentials and paths.

# -----------------------------------------------------------------------------
# Flag-token bookkeeping
# -----------------------------------------------------------------------------

# loop_gen_or_reuse_flags RUN_ID OUT_DIR
#
# Ensures OUT_DIR/flags.env exists for the given RUN_ID. If the file is
# already present we reuse it (regenerating tokens after a Packer rebuild
# would desync the on-disk flags from what is baked into the qcow2).
# Otherwise we write fresh hex-12 tokens with mode 0600.
#
# Side effects:
#   - mkdir -p OUT_DIR
#   - writes / sources OUT_DIR/flags.env
#   - exports SECRETCON_USER_FLAG, SECRETCON_ROOT_FLAG, SECRETCON_RUN_ID
#   - echoes the absolute path to flags.env on stdout (so callers can
#     capture it with `FLAGS_ENV=$(loop_gen_or_reuse_flags ...)`).
loop_gen_or_reuse_flags() {
    local run_id="$1" out_dir="$2"
    mkdir -p "$out_dir"
    local flags_env="${out_dir}/flags.env"

    if [ -f "$flags_env" ]; then
        echo "[*] flags.env already exists, reusing: ${flags_env}" >&2
        # shellcheck disable=SC1090
        . "$flags_env"
        echo "    user: ${SECRETCON_USER_FLAG:-<missing>}" >&2
        echo "    root: ${SECRETCON_ROOT_FLAG:-<missing>}" >&2
        export SECRETCON_USER_FLAG SECRETCON_ROOT_FLAG SECRETCON_RUN_ID
        echo "$flags_env"
        return 0
    fi

    local user_tok root_tok
    user_tok=$(openssl rand -hex 12)
    root_tok=$(openssl rand -hex 12)

    cat > "$flags_env" <<EOF
# Generated by scripts/lib/loop_lib.sh::loop_gen_or_reuse_flags on $(date -u +%FT%TZ)
# Run ID: ${run_id}
SECRETCON_USER_FLAG=flag{user-${user_tok}}
SECRETCON_ROOT_FLAG=flag{root-${root_tok}}
SECRETCON_RUN_ID=${run_id}
EOF
    chmod 0600 "$flags_env"

    # shellcheck disable=SC1090
    . "$flags_env"
    export SECRETCON_USER_FLAG SECRETCON_ROOT_FLAG SECRETCON_RUN_ID

    echo "[*] flags.env written: ${flags_env}" >&2
    echo "    user: ${SECRETCON_USER_FLAG}" >&2
    echo "    root: ${SECRETCON_ROOT_FLAG}" >&2
    echo "$flags_env"
}

